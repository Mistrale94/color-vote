import Head from 'next/head'
import Image from 'next/image'
import clientPromise from '../lib/mongodb'
import { useEffect, useState } from 'react'

export default function Home({isConnected}) {
  const [restaurants, setRestaurants] = useState([]);

  useEffect(() => {
    (async () => {
      const results = await fetch('/api/list');
      const resultsJson = await results.json();
      setRestaurants(resultsJson);
    })();
  }, []);

  return (
    <div>
      <Head>
        <title>Accueil</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <h1 className="text-3xl font-bold underline">
          Hello world!
        </h1>

        <figure>
          <Image src="/image/logo.png" width={300} height={300} alt="Logo" />
        </figure>
        <h1>Code pin</h1>

        <form>
          <input type="text" placeholder='Code de la session'></input>
          <input type="text" placeholder='PrÃ©nom'></input>
          <button type="submit">Rejoindre la session</button>
        </form>

        <div>
          {restaurants.map((restaurant) => (
            <div className='card' key={restaurant.id}>
              <h2>{restaurant.name}</h2>
              <p>{restaurant.cuisine}</p>
            </div>
          ))}
        </div>
      </main>
    </div>
  )
}

export async function getServerSideProps(context) {
  try{
    await clientPromise
    return {
      props: { isConnected: true },
    }
  } catch (e) {
    console.error(e);
    return {
      props: { isConnected: false },
    }
  }
}